export function getT(lang='pt'){
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.pt;
  return (key, vars) => {
    let s = dict[key] ?? key;
    if(vars) Object.keys(vars).forEach(k => s = s.replace(`{${k}}`, vars[k]));
    return s;
  };
}

export function labelCategory(section, key, lang='pt'){
  const d = TRANSLATIONS[lang] || TRANSLATIONS.pt;
  if(section === 'entradas') return d.cat_entradas?.[key] ?? key;
  if(section === 'principais') return d.cat_principais?.[key] ?? key;
  return key;
}

export function labelDish(id, fallbackName, lang='pt'){
  const d = TRANSLATIONS[lang] || TRANSLATIONS.pt;
  return d.dishes?.[id] ?? fallbackName;
}
